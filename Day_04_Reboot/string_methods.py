s = "hello world"

print(s.upper(), s)  # Return a copy of the string converted to uppercase and does not changed the original string.

print(s.lower(), s)  # Return a copy of the string converted to lowercase and does not changed the original string.

print(len(s))        # Return Length of the string.

print(s.capitalize(), s) # Return first character Upper case

print(s.title(), s)  # Return a version of the string where each word is titlecased.