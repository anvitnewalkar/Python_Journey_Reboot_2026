""" 
Create a list of numbers from 1 to 10 .
Print the first three numbers using slicing.
Print the last three numbers using slicing.

"""

list_1 = [i for i in range(1,11)]

print(list_1)

# Print the first 3 numbers using slicing

print(list_1[0:3]) #[:3] alternate writing method


# Print the last 3 numbers using slicing

print(list_1[-3:])

