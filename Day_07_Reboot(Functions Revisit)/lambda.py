# Whenever we wnat to define simple functions we use lambda function

# Lambda functions are anonymous inline function

square = lambda x: x * x
print(f"The square of x is:-{square(8)}")

sum = lambda a,b: a + b

print(f"Sum is:-{sum(10,20)}")


